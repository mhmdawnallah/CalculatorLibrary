"""Calculator library containing basic math operations"""


def add(first_term, second_term):
    """Add Two terms"""
    return first_term + second_term


def substract(first_term, second_term):
    """Substract Two Terms"""
    return first_term - second_term

def multiply(first_term, second_term):
    """Multiply Two Terms"""
    return first_term * second_term
